#include "utils.h"

int	ft_ismetachar(int c)
{
	if (c == '>' || c == '<' || c == '*' || c == '?' || c == '[' || c == ']'
		|| c == '`' || c == '$' || c == '|' || c == ';' || c == '&'
		|| c == '(' || c == ')' || c == '#' || c == '\\')
		return (1);
	return (0);
}
